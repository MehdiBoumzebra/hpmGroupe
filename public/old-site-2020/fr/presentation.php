                                 <h3>La société</h3>
                                 <p>
                                     <span>Horizon Project Management</span> est une société d’ingénierie en conseil, études techniques et <span></span>management de projets</span>, dans le domaine du bâtiment et des travaux publics.
<br><br>
Spécialisée dans les études, le pilotage et la coordination de projets immobiliers, <span>HPM</span> a pour vocation d’aider le donneur d’ordre, promoteur immobilier ou toute société privée ou publique, dans l’élaboration et la mise en œuvre de son <span>projet de construction.</span>
<br><br>
HPM est votre partenaire privilégié dès la phase amont de votre projet (études préalables, programmation) et  jusqu’à la fin du projet de construction (déclaration d’achèvement des travaux, solde des marchés, DGD).
<br><br>
Horizon Project Management est basée au <span>Maroc</span>, et intervient également à <span>l’international</span> pour des prestations d’<span>Assistance à Maîtrise d’Ouvrage, Maîtrise d’Ouvrage Déléguée et Maîtrise d’Œuvre ( Direction des Travaux, OPC et Maîtrise d'Oeuvre Technique ).</span>
